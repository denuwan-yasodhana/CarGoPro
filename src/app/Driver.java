    // Edit driver details
    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        if (txt_driverID.getText().isEmpty() || txt_driverName.getText().isEmpty() || txt_driTele.getText().isEmpty() || txt_driNIC.getText().isEmpty() || cmb_status.getSelectedIndex() == 0 )
        {
            JOptionPane.showMessageDialog(this,"Select the Driver to be Updated");
        }
        else
        {
            try 
            {
                con = conManager.createConnection();
                
                String DID = txt_driverID.getText();
                String Query = "Update  car_rental_system.drivers set Name= '"+txt_driverName.getText()+ "', Status='"+cmb_status.getSelectedItem().toString()+"', TelephoneNo='"+txt_driTele.getText()+"', NIC = '"+txt_driNIC.getText()+"' where DriverID ='"+DID+"'" ;
                Statement Add =  con.createStatement();
                Add.executeUpdate(Query);
                JOptionPane.showMessageDialog(this,"Driver details Updated successfuly");


                DisplayDrivers();
                Reset();


            }catch (Exception e){
               e.printStackTrace();
            }
        } 
    }//GEN-LAST:event_btn_editActionPerformed

    private void table_driversMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_driversMouseClicked
        DefaultTableModel model = (DefaultTableModel)table_drivers.getModel();
        int MyIndex = table_drivers.getSelectedRow();
        txt_driverID.setText(model.getValueAt(MyIndex, 0).toString());
        txt_driverName.setText(model.getValueAt(MyIndex, 1).toString());
        txt_driTele.setText(model.getValueAt(MyIndex, 3).toString());
        cmb_status.setSelectedItem(model.getValueAt(MyIndex, 2).toString());
        txt_driNIC.setText(model.getValueAt(MyIndex, 4).toString());
        
        
    }//GEN-LAST:event_table_driversMouseClicked

