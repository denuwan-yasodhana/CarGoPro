    // Display full details of drivers
     private void DisplayDrivers()
     {
         try{
            // Make connection 
            
            con = conManager.createConnection();
           
            String sql = "SELECT * FROM drivers";
            PreparedStatement pstmt = con.prepareStatement(sql);
            rs = pstmt.executeQuery();
            
            // Set to table
            table_drivers.setModel(DbUtils.resultSetToTableModel(rs));
         
         } 
         catch (SQLException e)
         {
            e.printStackTrace();
         
         }
     } 

     // Save driver details
    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (txt_driverID.getText().isEmpty()|| txt_driverName.getText().isEmpty() || txt_driTele.getText().isEmpty() || txt_driNIC.getText().isEmpty() || cmb_status.getSelectedIndex() == 0 )
        {
            JOptionPane.showMessageDialog(this,"Missing Information");
        }
        else
        {
            try 
            {
                con = conManager.createConnection();
                PreparedStatement add = con.prepareStatement("insert into drivers values(?, ?, ?, ?, ?)");
                
                // Get details from textboxs
                add.setString(1, txt_driverID.getText());
                add.setString(2, txt_driverName.getText());
                add.setInt(4, Integer.valueOf(txt_driTele.getText()));
                add.setString(3, cmb_status.getSelectedItem(). toString());
                add.setString(5, txt_driNIC.getText());
                
                int row =  add.executeUpdate();
                JOptionPane.showMessageDialog(this,"Driver details Added successfuly");

                DisplayDrivers();
                Reset();

            }
            catch (Exception e){
               e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

    // Delete driver details
    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
       
        if (txt_driverID.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this,"Select the Driver to be Deleted");
        }
        else
        {
            try 
            {
                con = conManager.createConnection();
                
                String DID = txt_driverID.getText();
                String Query = "Delete from car_rental_system.drivers where DriverID = '"+ DID +"'" ;
                Statement Add =  con.createStatement();
                Add.executeUpdate(Query);
                JOptionPane.showMessageDialog(this,"Driver details Deleted successfuly");

                DisplayDrivers();
                Reset();


            }
            catch (Exception e)
            {
               e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

