    //display customerID
    private void displayid(){
        
        try{
        con = conManager.createConnection();
        st=con.createStatement();
        
        rs=st.executeQuery("SELECT MAX(CustomerID) from customerdata");
        rs.next();
        
        rs.getString("MAX(CustomerID)");
        
        if( rs.getString("MAX(CustomerID)")==null){
            
            txt_cusID.setText("CID1");
        
            }
       
        else{
                Long id=Long.parseLong(rs.getString("MAX(CustomerID)").substring(3,rs.getString("MAX(CustomerID)").length())); 
                id++;
                txt_cusID.setText("CID"+String.format("%d",id));
                
            }
        
        txt_cusID.setEditable(false);
        }
        
        catch (SQLException e)
        {
            e.printStackTrace();
         
        }
    }

    // Display full details of customers
     private void DisplayCustomers()
     {
         try{
            // Make connection 
            
            con = conManager.createConnection();
            String sql = "SELECT * FROM customerdata";
            PreparedStatement pstmt = con.prepareStatement(sql);
            rs = pstmt.executeQuery();
            
            // Set to table
            table_customer.setModel(DbUtils.resultSetToTableModel(rs));
         
         } 
         catch (SQLException e)
         {
            e.printStackTrace();
         
         }
     } 

     // Save customer details
    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (txt_cusID.getText().isEmpty()|| txt_cusName.getText().isEmpty() || txt_address.getText().isEmpty() || txt_phone.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Missing Information");
        }
        else
        {
            try 
            {
                Session s = connection.Controller.getSessionFactory().openSession();
                Transaction tr = s.beginTransaction();

                pojos.Customerdata cust = new pojos.Customerdata();
                cust.setCustomerId(txt_cusID.getText());
                cust.setName(txt_cusName.getText());
                cust.setAddress(txt_address.getText());
                cust.setTelephoneNo(Integer.valueOf(txt_phone.getText()));

                s.save(cust);
                tr.commit();


                s.close();
                tr = null;
                JOptionPane.showMessageDialog(this,"Customer Details Added successfuly");

                DisplayCustomers();
                Reset();

            }
            catch (Exception e){
               e.printStackTrace();
            }
        }

    
        

    }//GEN-LAST:event_btn_saveActionPerformed

    // Delete customer details
    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
       
        if (txt_cusID.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this,"Select the customer to be Deleted");
        }
        else
        {
            try 
            {
                con = conManager.createConnection();
                
                String CusID = txt_cusID.getText();
                String Query = "Delete from car_rental_system.customerdata where CustomerID = '"+ CusID +"'" ;
                Statement Add =  con.createStatement();
                Add.executeUpdate(Query);
                JOptionPane.showMessageDialog(this,"Customer details Deleted successfuly");

                DisplayCustomers();
                Reset();


            }
            catch (Exception e)
            {
               e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed
