    // Display full details of cars
     private void DisplayCars()
     {
         try{
            // Make connection 
            con = conManager.createConnection();
           
            String sql = "SELECT * FROM cars";
            PreparedStatement pstmt = con.prepareStatement(sql);
            rs = pstmt.executeQuery();
            
            // Set to table
            table_cars.setModel(DbUtils.resultSetToTableModel(rs));
         
         } 
         catch (SQLException e)
         {
            e.printStackTrace();
         
         }
     } 

     // Save car details
    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (txt_regno.getText().isEmpty()|| txt_brand.getText().isEmpty() || txt_model.getText().isEmpty() || txt_price.getText().isEmpty() || cmb_status.getSelectedIndex() == 0 )
        {
            JOptionPane.showMessageDialog(this,"Missing Information");
        }
        else
        {
            try 
            {
               con = conManager.createConnection();
                PreparedStatement add = con.prepareStatement("insert into cars values(?, ?, ?, ?, ?)");
                
                // Get details from textboxs
                add.setString(1, txt_regno.getText());
                add.setString(2, txt_brand.getText());
                add.setString(3, txt_model.getText());
                add.setString(4, cmb_status.getSelectedItem(). toString());
                add.setInt(5, Integer.valueOf(txt_price.getText()));
                
                int row =  add.executeUpdate();
                JOptionPane.showMessageDialog(this,"Car Added successfuly");

                DisplayCars();
                Reset();

            }
            catch (Exception e){
               e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

    // Delete car details
    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
       
        if (txt_regno.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this,"Select the car to be Deleted");
        }
        else
        {
            try 
            {
                con = conManager.createConnection();
                
                String Reg = txt_regno.getText();
                String Query = "Delete from car_rental_system.cars where CarID = '"+ Reg +"'" ;
                Statement Add =  con.createStatement();
                Add.executeUpdate(Query);
                JOptionPane.showMessageDialog(this,"Car Deleted successfuly");

                DisplayCars();
                Reset();


            }
            catch (Exception e)
            {
               e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

