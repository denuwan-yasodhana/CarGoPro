    // Edit car details
    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        if (txt_regno.getText().isEmpty() || txt_brand.getText().isEmpty() || txt_model.getText().isEmpty() || txt_price.getText().isEmpty() || cmb_status.getSelectedIndex() == 0 )
        {
            JOptionPane.showMessageDialog(this,"Select the car to be Updated");
        }
        else
        {
            try 
            {
                con = conManager.createConnection();
                
                String reg = txt_regno.getText();
                String Query = "Update  car_rental_system.cars set Brand= '"+txt_brand.getText()+ "',Model='"+txt_model.getText()+"',Status='"+cmb_status.getSelectedItem().toString()+"',Price = '"+txt_price.getText()+"' where CarID ='"+reg+"'" ;
                Statement Add =  con.createStatement();
                Add.executeUpdate(Query);
                JOptionPane.showMessageDialog(this,"Car Updated successfuly");


                DisplayCars();
                Reset();


            }catch (Exception e){
               e.printStackTrace();
            }
        } 
    }//GEN-LAST:event_btn_editActionPerformed

    private void table_carsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_carsMouseClicked
        DefaultTableModel model = (DefaultTableModel)table_cars.getModel();
        int MyIndex = table_cars.getSelectedRow();
        txt_regno.setText(model.getValueAt(MyIndex, 0).toString());
        txt_brand.setText(model.getValueAt(MyIndex, 1).toString());
        txt_model.setText(model.getValueAt(MyIndex, 2).toString());
        cmb_status.setSelectedItem(model.getValueAt(MyIndex, 3).toString());
        txt_price.setText(model.getValueAt(MyIndex, 4).toString());
        
        
    }//GEN-LAST:event_table_carsMouseClicked

